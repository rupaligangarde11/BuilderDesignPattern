public class Pizza {
    private String dough;
    private String toppings;
    private String sauce;

    public void setDough(String dough) {
        this.dough = dough;
    }

    public void setToppings(String toppings) {
        this.toppings = toppings;
    }

    public void setSauce(String sauce) {
        this.sauce = sauce;
    }
}
