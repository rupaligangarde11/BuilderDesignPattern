class Pizza {
    private String dough ;
    private String toppings ;
    private String sauce;

    void setDough(String dough) {
        this.dough = dough;
    }

    void setToppings(String toppings) {
        this.toppings = toppings;
    }

    void setSauce(String sauce) {
        this.sauce = sauce;
    }
}
